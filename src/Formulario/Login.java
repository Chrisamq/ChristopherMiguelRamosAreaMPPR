    private void txtContraseñaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraseñaKeyTyped
        char car = evt.getKeyChar();
        String cadena = ",-_{}[¨*+´!°#$%&/()=?¡.;;><"; // Caracterens no validos
        char s = '"';
        for (int i = 0; i < cadena.length(); i++) {
            if (car == cadena.charAt(i) || car == s) {
                evt.consume();
            }
        }
        if (txtContraseña.getPassword().length == 15) {
            evt.consume();
        }
    }//GEN-LAST:event_txtContraseñaKeyTyped

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

 try {
            E_Usuario objUsuario;
            C_Usuario ctrlUsuario = new C_Usuario();

            objUsuario = ctrlUsuario.seleccionarUsuario(txtUsuario.getText().trim(), txtContraseña.getText().trim());
            if (objUsuario != null) {

                JOptionPane.showMessageDialog(this, "Bienvenido al Sistema");

                MenuJefe principal = new MenuJefe();
                principal.setVisible(true);
                salir();
            } else {
                JOptionPane.showMessageDialog(this, "Usuario Invalido");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error de Coneccion", "Usuarios", JOptionPane.ERROR_MESSAGE);
        }




    }//GEN-LAST:event_btnAceptarActionPerformed

